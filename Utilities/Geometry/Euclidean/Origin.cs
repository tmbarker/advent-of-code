namespace Utilities.Geometry.Euclidean;

/// <summary>
///     Represents the convention of a <see cref="Euclidean" /> coordinate system origin
/// </summary>
public enum Origin
{
    /// <summary>
    ///     Y increases as you move upwards
    /// </summary>
    Xy,

    /// <summary>
    ///     Y increases as you move downwards
    /// </summary>
    Uv
}